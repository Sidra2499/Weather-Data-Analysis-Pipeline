import requests
import pandas as pd
from datetime import datetime

# API Key
API_KEY = "d57dc202e4c83abac81f9d8c32456bc8"
BASE_URL = "https://api.openweathermap.org/data/2.5/weather"

# Cities to fetch
cities = ["Lahore", "Karachi", "Islamabad", "Faisalabad", "Sialkot", "Sheikhupura",
          "Peshawar", "Skardu", "Gilgit", "Hunza"]

# List to store results
weather_data = []

for city in cities:
    params = {
        "q": city,
        "appid": API_KEY,
        "units": "metric"
    }
    response = requests.get(BASE_URL, params=params)
    if response.status_code == 200:
        data = response.json()
        weather_data.append({
            "City": city,
            "Temperature (Â°C)": data["main"]["temp"],
            "Feels Like (Â°C)": data["main"]["feels_like"],
            "Humidity (%)": data["main"]["humidity"],
            "Wind Speed (m/s)": data["wind"]["speed"],
            "Weather": data["weather"][0]["description"],
            "Time": datetime.utcfromtimestamp(data["dt"]).strftime('%Y-%m-%d %H:%M:%S')
        })
    else:
        print(f"âš ï¸ Failed to fetch data for {city}")

